package Relationship;

import java.util.Random;

public class Lazel extends NPC {

    public Lazel() {
        super("Lazel");
        //heartLevel = 2  ;
    }

    @Override
    protected String getRandomDialogue(int level) {
        String[] dialogues;
        if (level < 2) {
            dialogues = new String[]{
                "Chk! อย่ามายืนเกะกะ ข้าต้องฝึกดาบ",
                "มองหน้าทำไม? อยากโดนเฉือนจมูกหรือไง?",
                "อ่อนแอ... สิ่งมีชีวิตที่นี่ช่างเปราะบาง"
            };
        } else if (level < 4) {
            dialogues = new String[]{
                "ฝีมือการต่อสู้ของเจ้า... ก็พอใช้ได้ ไม่เลวนัก",
                "วันนี้เจ้าดู... แข็งแกร่งขึ้นนะ (หลบตา)",
                "เราจะล่านกอะไรมากินดีเย็นนี้?"
            };
        } else {
            dialogues = new String[]{
                "เจ้าคือคู่หูที่แข็งแกร่งที่สุดเท่าที่ข้าเคยมี",
                "ใครหน้าไหนกล้าแตะต้องเจ้า มันต้องผ่านดาบข้าไปก่อน",
                "มานี่สิ... ข้าอยาก... ดมกลิ่นเจ้าใกล้ๆ (หน้าแดง)"
            };
        }
        return dialogues[new Random().nextInt(dialogues.length)];
    }

    @Override
    protected String getSpecialScene(int level) {
        // คืนค่าเป็น Text ยาวๆ สำหรับใส่ใน SceneUpdate
        return switch (level) {
            case 1 -> "Lazel: Chk! ปฏิกิริยาช้าเป็นเต่า! ถ้าข้าเป็นศัตรู เจ้าตายไปแล้ว! ... แต่ก็นะ อย่างน้อยเจ้าก็ไม่วิ่งหนีข้าเหมือนคนอื่น หึ.. ก็ดี";
            case 2 -> "Lazel: ข้าเริ่มสงสัยในคำสอนของ Vlaakith... ไม่สิ! ลืมมันซะ! ... แค่การมีเจ้าอยู่ระวังหลังให้ข้า มันทำให้ข้ารู้สึก... ปลอดภัย แบบที่ไม่เคยเป็น";
            case 3 -> "Lazel: กินซะ! ข้าล่าส่วนที่ดีที่สุดมาให้เจ้า... นี่เป็นการแสดงความยอมรับในแบบของ Creche ข้า... เจ้าคือนักรบ และข้ายอมรับเจ้าเป็นสหาย";
            case 4 -> "Lazel: (เดินเข้ามาประชิดตัว) ข้าต้องการเจ้า... เดี๋ยวนี้... ร่างกายข้ามันเรียกร้องหาเจ้า ไม่ใช่การต่อสู้ แต่เป็นสัมผัส... เจ้าทำให้ข้าอ่อนแอลง แต่ข้ากลับชอบมัน";
            case 5 -> "Lazel: (จับมือคุณแน่น) ข้าเคยคิดว่าความรักคือความอ่อนแอ... แต่เจ้าสอนให้ข้ารู้ว่า มันคือพลังที่ยิ่งใหญ่กว่าอาวุธใดๆ... ข้าคือของเจ้า และเจ้าคือของข้า ชั่วนิรันดร์";
            default -> "...";
        };
    }
}